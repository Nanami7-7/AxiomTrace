/* ---------- I2C helpers ---------- */

static hal_status_t oled_write_cmd(uint8_t cmd)
{
    uint8_t buf[2] = { SSD1306_CTRL_CMD, cmd };
    return bsp_i2c0_write(BSP_OLED_I2C_ADDR, buf, 2, BSP_OLED_TIMEOUT_MS);
}

static hal_status_t oled_write_data(const uint8_t *data, uint16_t len)
{
    /* We need to prepend control byte 0x40 (data) to the payload.
     * For efficiency, send in chunks: control byte + up to 32 bytes data per I2C transaction.
     */
    hal_status_t status;
    uint16_t offset = 0;

    while (offset < len) {
        uint16_t chunk = len - offset;
        if (chunk > 31) chunk = 31;  /* Leave room for control byte */

        uint8_t buf[32];
        buf[0] = SSD1306_CTRL_DATA;
        memcpy(&buf[1], &data[offset], chunk);

        status = bsp_i2c0_write(BSP_OLED_I2C_ADDR, buf, chunk + 1, BSP_OLED_TIMEOUT_MS);
        if (status != HAL_OK) return status;

        offset += chunk;
    }

    return HAL_OK;
}

/* ========== API ========== */

hal_status_t bsp_oled_init(void)
{
    hal_status_t status;

    /* SSD1306 initialization sequence */
    status = oled_write_cmd(SSD1306_CMD_SET_DISPLAY_OFF);           if (status != HAL_OK) return status;
    status = oled_write_cmd(SSD1306_CMD_SET_DISPLAY_CLOCK_DIV);     if (status != HAL_OK) return status;
    status = oled_write_cmd(0x80);                                  if (status != HAL_OK) return status;  /* Div ratio */
    status = oled_write_cmd(SSD1306_CMD_SET_MULTIPLEX_RATIO);       if (status != HAL_OK) return status;
    status = oled_write_cmd(0x3F);                                  if (status != HAL_OK) return status;  /* 64 mux */
    status = oled_write_cmd(SSD1306_CMD_SET_DISPLAY_OFFSET);        if (status != HAL_OK) return status;
    status = oled_write_cmd(0x00);                                  if (status != HAL_OK) return status;  /* No offset */
    status = oled_write_cmd(SSD1306_CMD_SET_START_LINE | 0x00);    if (status != HAL_OK) return status;
    status = oled_write_cmd(SSD1306_CMD_SET_CHARGE_PUMP);           if (status != HAL_OK) return status;
    status = oled_write_cmd(0x14);                                  if (status != HAL_OK) return status;  /* Enable charge pump */
    status = oled_write_cmd(SSD1306_CMD_SET_MEMORY_ADDR_MODE);      if (status != HAL_OK) return status;
    status = oled_write_cmd(0x00);                                  if (status != HAL_OK) return status;  /* Horizontal mode */
    status = oled_write_cmd(SSD1306_CMD_SET_SEGMENT_REMAP);         if (status != HAL_OK) return status;  /* Column 127 → 0 */
    status = oled_write_cmd(SSD1306_CMD_SET_COM_SCAN_DIR);          if (status != HAL_OK) return status;  /* Remapped mode */
    status = oled_write_cmd(SSD1306_CMD_SET_COM_PINS);              if (status != HAL_OK) return status;
    status = oled_write_cmd(0x12);                                  if (status != HAL_OK) return status;
    status = oled_write_cmd(SSD1306_CMD_SET_CONTRAST);              if (status != HAL_OK) return status;
    status = oled_write_cmd(0xCF);                                  if (status != HAL_OK) return status;
    status = oled_write_cmd(SSD1306_CMD_SET_PRECHARGE);             if (status != HAL_OK) return status;
    status = oled_write_cmd(0xF1);                                  if (status != HAL_OK) return status;
    status = oled_write_cmd(SSD1306_CMD_SET_VCOM_DESELECT);         if (status != HAL_OK) return status;
    status = oled_write_cmd(0x40);                                  if (status != HAL_OK) return status;
    status = oled_write_cmd(SSD1306_CMD_SET_ENTIRE_DISPLAY_ON);     if (status != HAL_OK) return status;
    status = oled_write_cmd(SSD1306_CMD_SET_NORMAL_DISPLAY);        if (status != HAL_OK) return status;

    /* Clear frame buffer and display */
    memset(s_framebuf, 0, sizeof(s_framebuf));
    status = bsp_oled_update_display();
    if (status != HAL_OK) return status;

    /* Turn on display */
    status = oled_write_cmd(SSD1306_CMD_SET_DISPLAY_ON);
    return status;
}

hal_status_t bsp_oled_deinit(void)
{
    memset(s_framebuf, 0, sizeof(s_framebuf));
    bsp_oled_update_display();
    return oled_write_cmd(SSD1306_CMD_SET_DISPLAY_OFF);
}

hal_status_t bsp_oled_clear(void)
{
    memset(s_framebuf, 0, sizeof(s_framebuf));
    return HAL_OK;
}

hal_status_t bsp_oled_set_pixel(uint32_t x, uint32_t y, uint8_t color)
{
    if (x >= BSP_OLED_WIDTH || y >= BSP_OLED_HEIGHT) return HAL_INVALID;

    uint32_t page   = y / 8;
    uint32_t bitpos = y % 8;

    if (color) {
        s_framebuf[page * BSP_OLED_WIDTH + x] |=  (1 << bitpos);
    } else {
        s_framebuf[page * BSP_OLED_WIDTH + x] &= ~(1 << bitpos);
    }

    return HAL_OK;
}

hal_status_t bsp_oled_draw_hline(uint32_t x, uint32_t y, uint32_t len, uint8_t color)
{
    for (uint32_t i = 0; i < len; i++) {
        hal_status_t s = bsp_oled_set_pixel(x + i, y, color);
        if (s != HAL_OK) return s;
    }
    return HAL_OK;
}

hal_status_t bsp_oled_draw_vline(uint32_t x, uint32_t y, uint32_t len, uint8_t color)
{
    for (uint32_t i = 0; i < len; i++) {
        hal_status_t s = bsp_oled_set_pixel(x, y + i, color);
        if (s != HAL_OK) return s;
    }
    return HAL_OK;
}

hal_status_t bsp_oled_draw_line(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, uint8_t color)
{
    int32_t dx = (int32_t)x1 - (int32_t)x0;
    int32_t dy = (int32_t)y1 - (int32_t)y0;
    int32_t sx = (dx >= 0) ? 1 : -1;
    int32_t sy = (dy >= 0) ? 1 : -1;
    dx = (dx >= 0) ? dx : -dx;
    dy = (dy >= 0) ? dy : -dy;

    int32_t err = dx - dy;
    int32_t e2;

    while (1) {
        bsp_oled_set_pixel(x0, y0, color);
        if ((int32_t)x0 == (int32_t)x1 && (int32_t)y0 == (int32_t)y1) break;
        e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x0 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y0 += sy;
        }
    }
    return HAL_OK;
}

hal_status_t bsp_oled_fill_rect(uint32_t x, uint32_t y, uint32_t width, uint32_t height, uint8_t color)
{
    for (uint32_t row = 0; row < height; row++) {
        for (uint32_t col = 0; col < width; col++) {
            hal_status_t s = bsp_oled_set_pixel(x + col, y + row, color);
            if (s != HAL_OK) return s;
        }
    }
    return HAL_OK;
}

hal_status_t bsp_oled_write_char(uint32_t x, uint32_t y, char ch, bsp_oled_font_t font, uint8_t color)
{
    if (ch < 0x20 || ch > 0x7E) ch = '?';

    if (font == BSP_OLED_FONT_SMALL) {
        /* 6x8 font: y is in page units (0~7), each page = 8 pixels */
        if (y >= BSP_OLED_HEIGHT / 8) return HAL_INVALID;
        const uint8_t *glyph = s_font6x8[ch - 0x20];
        for (uint32_t i = 0; i < 6; i++) {
            if ((x + i) >= BSP_OLED_WIDTH) break;
            if (color) {
                s_framebuf[y * BSP_OLED_WIDTH + x + i] = glyph[i];
            } else {
                s_framebuf[y * BSP_OLED_WIDTH + x + i] = ~glyph[i];
            }
        }
    } else {
        /* 8x16 font: y is in pixel units (0~56) */
        if (y + 16 > BSP_OLED_HEIGHT) return HAL_INVALID;
        const uint8_t *glyph = s_font8x16[ch - 0x20];
        for (uint32_t i = 0; i < 8; i++) {
            if ((x + i) >= BSP_OLED_WIDTH) break;
            /* Upper half */
            uint32_t page_top = y / 8;
            uint8_t  shift_top = y % 8;
            if (color) {
                s_framebuf[page_top * BSP_OLED_WIDTH + x + i] |=  (glyph[i] << shift_top);
            } else {
                s_framebuf[page_top * BSP_OLED_WIDTH + x + i] &= ~(glyph[i] << shift_top);
            }
            /* Lower half */
            uint32_t page_bot = (y + 8) / 8;
            if (page_bot < BSP_OLED_HEIGHT / 8) {
                if (color) {
                    s_framebuf[page_bot * BSP_OLED_WIDTH + x + i] |=  (glyph[i + 8] >> (8 - shift_top));
                } else {
                    s_framebuf[page_bot * BSP_OLED_WIDTH + x + i] &= ~(glyph[i + 8] >> (8 - shift_top));
                }
            }
        }
    }

    return HAL_OK;
}

hal_status_t bsp_oled_write_string(uint32_t x, uint32_t y, const char *str, bsp_oled_font_t font, uint8_t color)
{
    if (str == NULL) return HAL_INVALID;

    uint32_t advance = (font == BSP_OLED_FONT_SMALL) ? 6 : 8;
    hal_status_t status = HAL_OK;

    while (*str) {
        status = bsp_oled_write_char(x, y, *str, font, color);
        if (status != HAL_OK) return status;
        x += advance;
        str++;
    }

    return HAL_OK;
}

hal_status_t bsp_oled_update_display(void)
{
    hal_status_t status;

    /* Set column address range: 0 ~ 127 */
    status = oled_write_cmd(SSD1306_CMD_SET_COLUMN_ADDR);
    if (status != HAL_OK) return status;
    oled_write_cmd(0x00);
    oled_write_cmd(BSP_OLED_WIDTH - 1);

    /* Set page address range: 0 ~ 7 */
    status = oled_write_cmd(SSD1306_CMD_SET_PAGE_ADDR);
    if (status != HAL_OK) return status;
    oled_write_cmd(0x00);
    oled_write_cmd((BSP_OLED_HEIGHT / 8) - 1);

    /* Send entire frame buffer */
    return oled_write_data(s_framebuf, sizeof(s_framebuf));
}
